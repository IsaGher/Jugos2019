    private void tfIdJugoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdJugoKeyTyped
        String id = String.valueOf(c1.verIdjugos());
        char letra = evt.getKeyChar();
        if(navegacion == false)this.tfIdJugo.setText(id);
        if (!Character.isLetter(letra) || Character.isLetter(letra)) {
            evt.consume();
        }
    }//GEN-LAST:event_tfIdJugoKeyTyped

