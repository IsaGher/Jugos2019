    private void tfnombreJugoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfnombreJugoKeyTyped
        char letra = evt.getKeyChar();
        String id = String.valueOf(c1.verIdjugos());
        if(navegacion == false)this.tfIdJugo.setText(id);
        if (!Character.isLetter(letra) && !Character.isSpaceChar(letra)) {
            evt.consume();
        }

    }//GEN-LAST:event_tfnombreJugoKeyTyped

