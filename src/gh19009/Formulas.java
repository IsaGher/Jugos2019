    private void bAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtrasActionPerformed
        this.navegacion = true;
        bSaNav.setVisible(navegacion);
        lNavegacion.setText("Navegacion activada");
        tfIdJugo.setEditable(false);
        tfnombreJugo.setEditable(false);
        mId.removeAllElements();
        mSaborS.removeAllElements();
        mSeleccion.removeAllElements();
        porcentaje = 0;
        lAviso.setText("");
        int id = Integer.parseInt(tfIdJugo.getText());
        if (id > 1) {
            id--;
            tfIdJugo.setText(String.valueOf(id));
            tfnombreJugo.setText(nombreJugo((id - 1)));
            idSa(id);
            cantidad(id);
            nomSabor();
        } else if(id == 0){
            String iD = String.valueOf(c1.verJugos()[0][0]);
            String nombre = String.valueOf(c1.verJugos()[0][1]);
            tfIdJugo.setText(iD);
            tfnombreJugo.setText(nombre);
            idSa(c1.verJugos()[0][0]);
            cantidad(c1.verJugos()[0][0]);
            nomSabor();
        }
    }//GEN-LAST:event_bAtrasActionPerformed

