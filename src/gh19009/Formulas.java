    private DefaultListModel idSa(Object idJugo) {
        for (int posicion = 0; posicion < c1.verMezclas().length; posicion++) {
            if (c1.verMezclas()[posicion][0].equals(idJugo)) {
                mId.addElement(c1.verMezclas()[posicion][1]);
            }
        }
        mId.addElement("");
        return mId;
    }

    private DefaultListModel nomSabor() {
        int cuenta = 0, pos;
        while (cuenta < (mId.getSize()-1)) {
            for (pos = 0; pos < VerTsabor().length; pos++) {
                if (mId.get(cuenta).equals(VerTsabor()[pos][0])) {
                    mSaborS.addElement(VerTsabor()[pos][1]);
                    cuenta++;
                }
            }
        }
        return mSaborS;
    }

    private DefaultListModel cantidad(Object idJugo) {
        for (int posicion = 0; posicion < c1.verMezclas().length; posicion++) {
            if (c1.verMezclas()[posicion][0].equals(idJugo)) {
                mSeleccion.addElement(c1.verMezclas()[posicion][2]);
            }
        }
        return mSeleccion;
    }

