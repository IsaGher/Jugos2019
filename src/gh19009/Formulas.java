    private void tfCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCantidadKeyTyped
        if (!Character.isDigit(evt.getKeyChar()) && evt.getKeyChar() != '.' && navegacion == true) {
            evt.consume();
        }
        if ((evt.getKeyChar() == '.') && ((tfCantidad).getText().contains("."))) {
            evt.consume();
        }
    }//GEN-LAST:event_tfCantidadKeyTyped

    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        if (navegacion == false && !tfCantidad.getText().isEmpty() && Double.parseDouble(tfCantidad.getText()) > 0 && Double.parseDouble(tfCantidad.getText()) <= 1.0) {
            mId.addElement(lsIdJugo.getSelectedValue());
            lsIdJugoS.setModel(mId);
            mSaborS.addElement(lsNombreJugo.getSelectedValue());
            lsNombreJugoS.setModel(mSaborS);
            mSeleccion.addElement(tfCantidad.getText());
            lsCantidadS.setModel(mSeleccion);
            porcentaje += Double.parseDouble(tfCantidad.getText());
            tfCantidad.setText("");
            String cantidad = "";
            if (porcentaje < 1.0) {
                cantidad = "Aviso: Falta " + (1.0 - porcentaje) + " para completar";
            } else if (porcentaje > 1.0) {
                cantidad = "Aviso: Se ha sobrepaso " + (porcentaje - 1.0);
            } else if (porcentaje == 1.0) {
                cantidad = "¡Formula Completa!";
            }
            lAviso.setText(cantidad);
        }
    }//GEN-LAST:event_bAgregarActionPerformed

