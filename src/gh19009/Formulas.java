    private void bProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bProcesarActionPerformed
        if (navegacion == false && porcentaje == 1.0 && !tfnombreJugo.getText().isEmpty()) {
            int posicion, idS, idJ = Integer.parseInt(tfIdJugo.getText());
            double cant;
            for (posicion = 0; posicion < mId.getSize(); posicion++) {
                idS = Integer.parseInt(String.valueOf(mId.getElementAt(posicion)));
                cant = Double.parseDouble(String.valueOf(mSeleccion.getElementAt(posicion)));
                c1.agregarMezclas(idJ, idS, cant);
            }
            c1.agregarJugo(tfnombreJugo.getText());
            ID = Integer.parseInt(tfIdJugo.getText());
            tfIdJugo.setText("");
            tfnombreJugo.setText("");
            mId.removeAllElements();
            mSaborS.removeAllElements();
            mSeleccion.removeAllElements();
            porcentaje = 0;
            lAviso.setText("Avisos");
        }
    }//GEN-LAST:event_bProcesarActionPerformed

