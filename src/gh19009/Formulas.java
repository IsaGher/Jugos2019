    private void bQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bQuitarActionPerformed
        if (navegacion == false) {
            mId.remove(lsIdJugoS.getSelectedIndex());
            mSaborS.remove(lsNombreJugoS.getSelectedIndex());
            porcentaje -= Double.parseDouble(lsCantidadS.getSelectedValue());
            mSeleccion.remove(lsCantidadS.getSelectedIndex());
            String cantidad = "";
            if (porcentaje < 1.0) {
                cantidad = "Aviso: Falta " + (1.0 - porcentaje) + " para completar";
            } else if (porcentaje > 1.0) {
                cantidad = "Aviso: Se ha sobrepaso " + (porcentaje - 1.0);
            } else if (porcentaje == 1.0) {
                cantidad = "¡Formula Completa!";
            }
            lAviso.setText(cantidad);
        }
    }//GEN-LAST:event_bQuitarActionPerformed

